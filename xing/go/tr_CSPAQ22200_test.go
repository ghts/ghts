package xing

import (
	lb "github.com/ghts/ghts/lib"
	"testing"
)

func TestCSPAQ22200_현물계좌_예수금_주문가능금액(t *testing.T) {
	t.Parallel()

	계좌번호, 에러 := F계좌_번호(0)
	lb.F테스트_에러없음(t, 에러)

	값, 에러 := TrCSPAQ22200_현물계좌_예수금_주문가능금액(계좌번호)
	lb.F테스트_에러없음(t, 에러)
	lb.F테스트_같음(t, 값.M계좌번호, 계좌번호)
	//lb.F테스트_다름(t, 값.M지점명, "")
	lb.F테스트_다름(t, 값.M계좌명, "")
	lb.F테스트_참임(t, 값.M현금주문가능금액 >= 0)
	lb.F테스트_참임(t, 값.M대용주문가능금액 >= 0)
	lb.F테스트_참임(t, 값.M코스피금액 >= 0)
	lb.F테스트_참임(t, 값.M코스닥금액 >= 0)
	lb.F테스트_참임(t, 값.M신용담보주문금액 >= 0)
	lb.F테스트_참임(t, 값.M증거금률100퍼센트주문가능금액 >= 0)
	lb.F테스트_참임(t, 값.M증거금률35퍼센트주문가능금액 >= 0)
	lb.F테스트_참임(t, 값.M증거금률50퍼센트주문가능금액 >= 0)
	lb.F테스트_참임(t, 값.M신용주문가능금액 >= 0)
	//lb.F테스트_참임(t, 값.M예수금 >= 0, 값.M예수금)
	lb.F테스트_참임(t, 값.M대용금액 >= 0)
	lb.F테스트_참임(t, 값.M증거금_현금 >= 0)
	lb.F테스트_참임(t, 값.M증거금_대용 >= 0)
	lb.F테스트_참임(t, 값.D1_예수금 >= 0)
	lb.F테스트_참임(t, 값.D2_예수금 >= 0)
	lb.F테스트_참임(t, 값.M미수금액 >= 0)
	lb.F테스트_참임(t, 값.D1연체변제소요금액 >= 0)
	lb.F테스트_참임(t, 값.D2연체변제소요금액 >= 0)
	lb.F테스트_같음(t, 값.M융자금액, 0)
	lb.F테스트_같음(t, 값.M변경후담보비율, 0.0)
	lb.F테스트_같음(t, 값.M소요담보금액, 0)
	lb.F테스트_같음(t, 값.M담보부족금액, 0)
	lb.F테스트_같음(t, 값.M원담보합계금액, 0)
	lb.F테스트_같음(t, 값.M부담보합계금액, 0)
	lb.F테스트_같음(t, 값.M신용담보금현금, 0)
	lb.F테스트_같음(t, 값.M신용담보대용금액, 0)
	lb.F테스트_같음(t, 값.M신용설정보증금, 0)
	lb.F테스트_같음(t, 값.M신용담보재사용금액, 0)
	lb.F테스트_같음(t, 값.M처분제한금액, 0)
	lb.F테스트_참임(t, 값.M전일매도정산금액 >= 0)
	lb.F테스트_참임(t, 값.M전일매수정산금액 >= 0)
	lb.F테스트_참임(t, 값.M금일매도정산금액 >= 0)
	lb.F테스트_참임(t, 값.M금일매수정산금액 >= 0)
	lb.F테스트_같음(t, 값.M매도대금담보대출금액, 0)
}
